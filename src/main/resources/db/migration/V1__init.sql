CREATE TABLE IF NOT EXISTS PESSOA(
  ID             BIGINT        NOT NULL AUTO_INCREMENT,
  NOME           VARCHAR(60)   NOT NULL,
  SEXO           VARCHAR(1),
  ESTADO_CIVIL   VARCHAR(1),
  CATEGORIA      VARCHAR(1)    NOT NULL,
  TIPO_PESSOA    VARCHAR(1)    NOT NULL,
  CPF_CNPJ       BIGINT,
  RG             BIGINT,
  IE             BIGINT,
  CEP            BIGINT        NOT NULL,
  ENDERECO       VARCHAR(60)   NOT NULL,
  COMPLEMENTO    VARCHAR(20),
  NRO            INTEGER,
  CIDADE         VARCHAR(60)   NOT NULL,
  BAIRRO         VARCHAR(30)   NOT NULL,
  UF             VARCHAR(2)    NOT NULL,
  TEL_CEL        BIGINT        NOT NULL,
  TEL_RES        BIGINT,
  EMAIL          VARCHAR(60),
  PRIMARY KEY PK_PESSOA (ID)
);
CREATE TABLE IF NOT EXISTS COMPRAS (
  ID             BIGINT        NOT NULL AUTO_INCREMENT,
  MARCA          VARCHAR(30)   NOT NULL,
  MODELO         VARCHAR(30)   NOT NULL,
  ANO_FABRICACAO DATE,
  ANO_MODELO     DATE,
  COR            VARCHAR(15),
  COMBUSTIVEL    VARCHAR(1),
  TIPO           VARCHAR(1)    NOT NULL,
  CATEGORIA      VARCHAR(1)    NOT NULL,
  CHASSI         VARCHAR(20),
  PLACA          VARCHAR(10),
  DATA_COMPRA    DATE,
  KM             INTEGER,
  ORIGEM         VARCHAR(60),
  PRECO_CUSTO    DECIMAL(15,2) NOT NULL,
  PRECO_VENDA    DECIMAL(15,2) NOT NULL,
  LUCRO          DECIMAL(8,2),
  QTDE           INTEGER,
  PRIMARY KEY PK_COMPRAS (ID)
);
CREATE TABLE IF NOT EXISTS VENDAS (
  ID             BIGINT        NOT NULL AUTO_INCREMENT,
  CLIENTE        VARCHAR(60)   NOT NULL,
  VENDEDOR       VARCHAR(60)   NOT NULL,
  VLR_TOTAL      DECIMAL(15,2),
  PRIMARY KEY PK_VENDAS (ID)
);